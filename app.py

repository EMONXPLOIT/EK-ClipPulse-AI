backend_app = FastAPI()

def extract_video_id(url):
    regex = r"(?:v=|\/)([0-9A-Za-z_-]{11}).*"
    match = re.search(regex, url)
    return match.group(1) if match else None

def get_transcript(video_id):
    try:
        transcript_list = YouTubeTranscriptApi.get_transcript(video_id, languages=['bn', 'en'])
        return "".join([f"[{t['start']:.2f}s]: {t['text']}\n" for t in transcript_list])
    except:
        return None

@backend_app.websocket("/ws/analyze")
async def websocket_endpoint(websocket_server: WebSocket):
    await websocket_server.accept()
    try:
        # ফ্রন্টএন্ড থেকে আসা ডেটা গ্রহণ
        data = await websocket_server.receive_text()
        payload = json.loads(data)
        video_url = payload.get("url")
        api_key = payload.get("api_key")
        
        await websocket_server.send_json({"status": "processing", "progress": 15, "message": "🤖 [EK-CORE] ইনপুট ডেটা ভ্যালিডেশন চেক করা হচ্ছে..."})
        await asyncio.sleep(0.5)
        
        video_id = extract_video_id(video_url)
        if not video_id:
            await websocket_server.send_json({"status": "error", "message": "❌ ভুল লিঙ্ক! সঠিক ইউটিউব ভিডিও ইউআরএল দিন।"})
            return
            
        await websocket_server.send_json({"status": "processing", "progress": 40, "message": "📡 [EK-CORE] ভিডিও থেকে সাবটাইটেল এক্সট্রাক্ট করা হচ্ছে..."})
        transcript = get_transcript(video_id)
        
        if not transcript:
            await websocket_server.send_json({"status": "error", "message": "⚠️ এই ভিডিওতে কোনো সাবটাইটেল পাওয়া যায়নি! অন্য ভিডিও ট্রাই করুন।"})
            return
            
        await websocket_server.send_json({"status": "processing", "progress": 70, "message": "🧠 [EK-CORE] Gemini 1.5 এআই ইঞ্জিনের সাথে যোগাযোগ স্থাপন করা হচ্ছে..."})
        
        # Gemini AI Setup
        genai.configure(api_key=api_key)
        model = genai.GenerativeModel("gemini-1.5-flash")
        
        prompt = f"""
        You are an expert viral video growth hacker. Analyze this transcript and return exactly 3 highly engaging segments (30-60s) perfect for TikTok/Shorts.
        Return raw JSON only, no markdown format like ```json.
        JSON format structure:
        [
          {{"start_time": float, "end_time": float, "duration": int, "reason": "Explain in Bengali why this part will go viral", "hook_title": "Catchy Bengali Short Title"}}
        ]
        Transcript:
        {transcript[:40000]}
        """
        
        await websocket_server.send_json({"status": "processing", "progress": 90, "message": "⚡ [EK-CORE] Gemini AI ভিডিওর সেরা ৩টি ভাইরাল মোメント প্রেডিক্ট করছে..."})
        
        response = model.generate_content(prompt)
        clean_text = response.text.replace("```json", "").replace("```", "").strip()
        results = json.loads(clean_text)
        
        # সফল রেসপন্স
        await websocket_server.send_json({
            "status": "success", 
            "progress": 100, 
            "message": "🔥 [EK-CORE] অ্যানালাইসিস সম্পূর্ণ সফল!", 
            "video_id": video_id,
            "data": results
        })
        
    except Exception as e:
        await websocket_server.send_json({"status": "error", "message": f"💥 সিস্টেম এরর: {str(e)}"})
    finally:
        await websocket_server.close()

